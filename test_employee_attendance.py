def bug(title, desc, ss_path=None):
    hdr = {"Authorization": f"token {GH_TOKEN}", "Accept": "application/vnd.github.v3+json"}
    img_md = ""
    if ss_path and os.path.isfile(ss_path):
        try:
            with open(ss_path, "rb") as f:
                b64 = base64.b64encode(f.read()).decode()
            ts = datetime.now().strftime("%Y%m%d_%H%M%S")
            rp = f"screenshots/{ts}_{os.path.basename(ss_path)}"
            r = requests.put(f"https://api.github.com/repos/{GH_REPO}/contents/{rp}",
                             headers=hdr, json={"message": f"Upload {os.path.basename(ss_path)}",
                                                "content": b64, "branch": "main"}, timeout=30)
            if r.status_code in (200, 201):
                dl = r.json().get("content", {}).get("download_url", "")
                if dl:
                    img_md = f"\n\n### Screenshot\n![screenshot]({dl})\n"
            else:
                print(f"    [WARN] SS upload HTTP {r.status_code}")
        except Exception as e:
            print(f"    [WARN] SS upload err: {e}")

    body = desc + img_md + "\n\n---\n_Auto-generated by E2E test suite_"
    r = requests.post(f"https://api.github.com/repos/{GH_REPO}/issues",
                      headers=hdr, json={"title": title, "body": body, "labels": ["bug"]}, timeout=30)
    if r.status_code == 201:
        url = r.json().get("html_url", "")
        print(f"    [BUG] {url}")
        bugs.append({"title": title, "url": url})
    else:
        print(f"    [WARN] Issue create HTTP {r.status_code}: {r.text[:200]}")


def click(driver, el):
    try:
        el.click()
    except ElementClickInterceptedException:
        driver.execute_script("arguments[0].click();", el)


def wait(driver, sec=2):
    time.sleep(sec)
    try:
        WebDriverWait(driver, 10).until(lambda d: d.execute_script("return document.readyState") == "complete")
    except:
        pass
    time.sleep(1)


def goto(driver, path):
    driver.get(BASE + path)
    wait(driver)


def page_has(driver, *words):
    src = driver.page_source.lower()
    return any(w in src for w in words)


def body_text(driver):
    return driver.find_element(By.TAG_NAME, "body").text


def headings(driver):
    return [h.text.strip() for h in driver.find_elements(By.CSS_SELECTOR, "h1, h2, h3") if h.is_displayed() and h.text.strip()]



def fill_add_form(driver, inputs):
    filled = []
    for inp in inputs:
        try:
            nm = (inp.get_attribute("name") or "").lower()
            ph = (inp.get_attribute("placeholder") or "").lower()
            lb = nm + " " + ph
            if any(w in lb for w in ["first", "fname"]):
                inp.clear(); inp.send_keys("Test"); filled.append("first_name")
            elif any(w in lb for w in ["last", "lname"]):
                inp.clear(); inp.send_keys("Employee"); filled.append("last_name")
            elif "email" in lb or "mail" in lb:
                inp.clear(); inp.send_keys("test.emp@technova.in"); filled.append("email")
            elif "phone" in lb or "mobile" in lb:
                inp.clear(); inp.send_keys("9876543210"); filled.append("phone")
        except:
            pass

    ss(driver, "add_emp_filled")
    log("Add employee fill", "PASS" if filled else "WARN", f"Filled: {filled}")

    # Try submit to check validation
    sub = None
    for b in driver.find_elements(By.TAG_NAME, "button"):
        t = (b.text or "").lower()
        if b.is_displayed() and any(w in t for w in ["save", "submit", "create", "add", "invite", "next"]):
            sub = b; break
    if not sub:
        try:
            sub = driver.find_element(By.CSS_SELECTOR, "button[type='submit']")
        except:
            pass

    if sub:
        click(driver, sub)
        time.sleep(2)
        s = ss(driver, "add_emp_submit")
        errs = [e for e in driver.find_elements(By.CSS_SELECTOR, "[class*='error'], [class*='invalid'], .text-danger, [role='alert']") if e.is_displayed() and e.text.strip()]
        if errs or page_has(driver, "required", "please enter", "mandatory"):
            log("Add emp validation", "PASS", f"{len(errs)} validation msgs")
        elif page_has(driver, "success", "created", "added"):
            log("Add emp submit", "PASS", "Success message")
        else:
            log("Add emp validation", "WARN", "No clear validation or success")
    else:
        log("Add emp submit btn", "WARN", "No submit button found")


